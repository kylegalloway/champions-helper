export function resolveMegaBase(speciesName: string): { base: string; item?: string } {
  const stone = megaForms[speciesName];
  if (!stone) return { base: speciesName };
  const base = speciesName.replace(/-Mega.*$/, '');
  return { base, item: stone };
}
